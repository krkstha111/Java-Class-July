package day7;

public class OperatorDemo4 {

	public static void main(String[] args) {
		
		
	// Relational Operators
		// >, <, >=, <=, ==, !=
		//Result- True, False
		int a =5;
		int b =6;
		System.out.println(a>b); // false
		System.out.println(a<b); //true
		System.out.println(a>=b);//false
		System.out.println(a<=b);//true
		System.out.println(a==b);//false
		System.out.println(a!=b);//true
		
		
	}
}
